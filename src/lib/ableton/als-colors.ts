export function getTrackColorIndex(hex: string): number {
  const upperHex = hex.toUpperCase();
  for (const [index, color] of Object.entries(ABLETON_TRACK_COLORS)) {
    if (color.toUpperCase() === upperHex) return Number(index);
  }
  return 0;
}

export function getTrackColorHex(index: number): string {
  return ABLETON_TRACK_COLORS[Math.max(0, Math.min(69, index))] ?? ABLETON_TRACK_COLORS[0];
}
